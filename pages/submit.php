<h1>Submission Guidelines</h1>
<p>The easiest way to to submit to MNKINO is to show up to one of our screenings with your video in hand. We prefer video files to DVD player format, which you can bring on a thumb drive or burn to a data DVD. We'd also appreciate it if you filled out the <a href="#form">submission form</a> below. Read the <a href="#notes">submission notes</a> for more detailed information.</p>

<h2 id="rules">Rules</h2>
<p>We have very few rules, so it should be pretty easy to follow 'em:
	<ol>
		<li>All submissions must be under 5 minutes in length!</li>
		<li>Submissions should have some relationship to the screening's theme/mystery meat.</li>
		<li>Give us a heads-up if your submission has "offensive" content.</li>
	</ol>
</p>
